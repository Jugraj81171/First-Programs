package april_20;

public class Franchise {

    int street_number;
    String street_name;
    String city;
    String postal_code;
    String country;

    public Franchise(int street_number, String street_name, String city, String postal_code, String country) {
        this.street_number = street_number;
        this.street_name = street_name;
        this.city = city;
        this.postal_code = postal_code;
        this.country = country;
    }
}
