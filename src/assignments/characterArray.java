package assignments;
/*create a char array characters A , B , C , D*/
public class characterArray {
    public static void main(String[] args) {

        String[] array={"A","B","C","D"};
        System.out.print("{ ");
        for(int i=0;i<array.length;i++)
        {
            System.out.print(array[i]+" ");
        }
        System.out.println("}");
    }


}

